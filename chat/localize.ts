export default function l(key: string, ...args: (string | number)[]): string {
    let i = args.length;
    let str = strings[key];
    if(str === undefined) {
        if(process.env.NODE_ENV !== 'production') throw new Error(`String ${key} does not exist.`);
        return '';
    }
    while(i-- > 0)
        str = str.replace(new RegExp(`\\{${i}\\}`, 'igm'), args[i].toString());
    return str;
}
